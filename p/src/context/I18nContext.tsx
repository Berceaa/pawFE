const I18nContext = createContext<I18nContextValue | undefined>(undefined);

function getByPath(tree: TranslationTree, path: string): TranslationLeaf {
  const value = path.split('.').reduce<unknown>((acc, key) => {
    if (acc && typeof acc === 'object' && key in (acc as Record<string, unknown>)) {
      return (acc as Record<string, unknown>)[key];
    }
    return undefined;
  }, tree);

  if (typeof value === 'string' || Array.isArray(value)) return value;
  return path;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    if (typeof window === 'undefined') return 'ro';
    return (window.localStorage.getItem('pawsentials-language') as Language) || 'ro';
  });

  useEffect(() => {
    window.localStorage.setItem('pawsentials-language', language);
  }, [language]);

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      t: (path: string) => getByPath(translations[language], path),
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }

  return context;
}
